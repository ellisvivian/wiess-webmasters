/*
 * Opens the BLM popup.
 */
function openBLMPopup() {
    document.getElementById('blm-popup').style.display = 'block';
}

/*
 * Closes the BLM popup.
 */
function closeBLMPopup() {
    document.getElementById('blm-popup').style.display = 'none';
}

